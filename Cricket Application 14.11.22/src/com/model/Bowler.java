package com.model;

public class Bowler {
	private String name;
	private int wicket, runs, bowledBalls;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getWicket() {
		return wicket;
	}

	public void setWicket(int wicket) {
		this.wicket = wicket;
	}

	public int getRuns() {
		return runs;
	}

	public void setRuns(int runs) {
		this.runs = runs;
	}

	public int getBowledBalls() {
		return bowledBalls;
	}

	public void setBowledBalls(int bowledBalls) {
		this.bowledBalls = bowledBalls;
	}

}
